// JS Properties and Methods

// indexOf (position)
var item = "bottle of water";
console.log(item.indexOf("w"));

// substring
var item = "bottle of water";
console.log(item.substring(7, 9));

// replace
var item = "bottle of water";
console.log(item.replace("water", "juice"));

// toUpperCase
var item = "bottle of water";
console.log(item.toUpperCase());

// Array Join
var items = ["bottle of water", 4, true];
console.log(items.join(" + "));

// Pop
var items = ["bottle of water", 4, true];
items.pop();
console.log(items.join(", "));

// Push
var items = ["bottle of water", 4, true];
items.push("ELBOW")
console.log(items);



